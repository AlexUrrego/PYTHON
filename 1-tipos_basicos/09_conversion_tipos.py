# tipo de datos, int es para numeros enteros, float para decimales, str para texto,
# bool  para verdadero o falso y char para solo una letra
print(bool(""))  # devuelve False porque no hay nada en el parentesis
print(bool("0"))  # devuelve True por que tiene algo en los parentesis
print(bool(None))  # Devuelve False porque None es como si no hubiera nada
print(bool(" "))  # devuelve False porque esta vacia
print(bool(0))  # Devuelve False porque es cero
