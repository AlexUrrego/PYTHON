print(1 > 2)  # False, significa 1 no es  mayor que 2
print(1 < 2)  # True, significa que 1 es  menor que 2
print(1 >= 2)  # False, significa que 1 es  mayor o igual a 2
print(1 <= 2)  # True, significa que 1 es   menor o igual a 2
print(1 == 2)  # False, significa que 1  es distinto de 2
print(1 != 2)  # True, sgnifica que 1  es diferente de 2
